package ProgLogicDBController;

import java.io.BufferedReader;
import java.io.FileReader;
import java.io.IOException;
import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.SQLException;
import java.util.Properties;
import javax.swing.JOptionPane;

/**
 *
 * @author HASAN
 */
public class DBConnect {

    private static final String DATABASE_DRIVER = "com.mysql.jdbc.Driver";
    private static String DATABASE_URL = "";
    private static final String USERNAME = "srmproject";
    private static final String PASSWORD = "srmsrm";
    private static final String MAX_POOL = "250";

    // init connection object
    private Connection connection;
    // init properties object
    private Properties properties;

    // create properties
    private Properties getProperties() {
        if (properties == null) {
            properties = new Properties();
            properties.setProperty("user", USERNAME);
            properties.setProperty("password", PASSWORD);
            properties.setProperty("MaxPooledStatements", MAX_POOL);
        }
        return properties;
    }

    // connect database
    public Connection connect() {
        BufferedReader br = null;
        String sCurrentLine;
        try {
            br = new BufferedReader(new FileReader("C:\\server.txt"));

            while ((sCurrentLine = br.readLine()) != null) {
//            	System.out.println(sCurrentLine);
                DATABASE_URL = "jdbc:mysql://" + sCurrentLine + ":3306/proglogic";
//                JOptionPane.showMessageDialog(null, DATABASE_URL);
            }
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (br != null) {
                    br.close();
                }
            } catch (IOException ex) {
                ex.printStackTrace();
            }
        }

        if (connection == null) {
            try {
                Class.forName(DATABASE_DRIVER);
                connection = DriverManager.getConnection(DATABASE_URL, getProperties());
            } catch (ClassNotFoundException | SQLException e) {
                e.printStackTrace();
            }
        }
        return connection;
    }

    // disconnect database
    public void disconnect() {
        if (connection != null) {
            try {
                connection.close();
                connection = null;
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
}
